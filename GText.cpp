void text(String characters, int x, int y, int size, RenderWindow& window) {
    int currentPoint = 0;
    for(int i = 0; i < characters.getSize(); i++) {
	int greatestPoint = 0;
        for(int y2 = 0; y2 < 20; y2++) {
	    for(int x2 = 0; x2 < letters[(int)characters[i] - 97][y2].getSize(); x2++) {
                if(letters[(int)characters[i] - 97][y2][x2] == '0') {
		    if(greatestPoint < x2) greatestPoint = x2;
	            RectangleShape thing;
		    thing.setPosition(x + x2 * size + currentPoint * size, y + y2 * size);
	            thing.setSize(Vector2f(size,size));
		    thing.setFillColor(Color::White);
	            window.draw(thing);
		}
	    }
        }
       currentPoint += greatestPoint + 2;
    }
}